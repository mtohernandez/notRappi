class Product {
  //Fields
  final int id;
  final String name;
  final String description;
  final double price;
  final String category;
  final List<String> additions;
  final String imgUrl; 

  //Constructor
  Product({
    required this.id, 
    required this.name, 
    required this.description, 
    required this.price, 
    required this.category, 
    required this.additions, 
    required this.imgUrl
  });
}




